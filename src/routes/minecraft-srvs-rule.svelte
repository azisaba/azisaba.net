<h1>各サーバールール</h1>

<p>アジ鯖のルールは、全体サーバールールと各サーバールールの2つがあります。</p>

<p>全体サーバールールはアジ鯖のサーバー全体で適用されるルールです。これに違反すると、アジ鯖全体で処罰がなされます。</p>
<p>各サーバールールは、各サーバー(LifeやLeonGunWarなど)で適用されるルールです。これに違反すると、その違反したサーバーで処罰がなされます。</p>

<p>各鯖ルールはサーバー一覧より、各サーバールールへのリンクがありますのでそちらをご覧ください。</p>

