<h1>Minecraftサーバ個別規定</h1>

<h3>第１条（目的）</h3>
<p>この個別規定は、アジ鯖が提供するMinecraftサーバにおけるサービスやコンテンツの利用条件を定めることを目的とします。</p>


<h3>第２条（定義）</h3>
<p>
この個別規定で使用する用語を、以下のように定義します。<br>
ア．「本個別規定」とは、この個別規約のことを指します。<br>
イ．「当団体」とは、アジ鯖のことを指します。<br>
ウ．「公式ホームページ」とは、アジ鯖が運営する公式ホームページ（https://www.azisaba.net）のことを指します。<br>
エ．「当サービス」とは、アジ鯖が運営するMinecraftサーバによるマルチプレイ環境を提供するサービスのことを指します。<br>
オ．「当サービス等」とは、アジ鯖が運営するMinecraftサーバによるマルチプレイ環境を提供するサービスやコンテンツのことを指します。<br>
ウ．「利用者」とは、当サービスに接続し利用する人物のことを指します。<br>
キ．「所属サーバ」とは、当サービスにおいて、テーマをもってMinecraftがプレイできる環境を提供しているサーバのことを指します。<br>
カ．「全体サーバ」とは、全ての所属サーバを指します。<br>
キ．「全体サーバルール」とは、全体サーバで共通して適用されるサーバルールのことを指します。<br>
ク．「各サーバルール」とは、所属サーバのみで適用されるサーバルールのことを指します。<br>
</p>


<h3>第３条（利用条件）</h3>
<p>
利用者が本サービス等を利用するには以下の条件を全て満たす必要があります。<br>
ア．満18歳を超えている方、または満18歳未満で保護者の同意がある方<br>
イ．正式な方法で購入したMinecraftをプレイできるアカウントを保持していること<br>
</p>


<h3>第４条（禁止事項）</h3>
<p>
利用者は、当サービス等を利用するにあたって以下の各号に定めることを行ってはいけません。<br>
ア．「MINECRAFT 利用規約および エンド ユーザー使用許諾契約書」に違反する行為<br>
イ．当団体への寄付によって入手したアイテムやその権利を第３者に売買する行為<br>
ウ．当サービス等の提供を中断している際に、複数回に渡って執拗に接続を試みる行為<br>
エ．当サービス等の利用が制限されているアカウントにおいて、制限等がなされた事項を行うまたはしようとする行為<br>
オ．当サービス等の利用が制限されているアカウントの所有者が、制限等がされていないアカウントを用いて制限等がなされた事項を行う、またはしようとする行為<br>
カ．利用者間で、当サービス等で入手できるアイテムや権利の貸与または譲渡、利用者間で取り決められた役務によって発生した対価を、当団体ではない団体が提供するサービス等で弁済するまたはしようとする行為<br>
キ．利用者間で、当サービス等で入手できるアイテムや権利の貸与または譲渡、利用者間で取り決められる役務を用いて、当団体ではない団体が提供するサービス等で発生した対価を弁済するまたはしようとする行為<br>
</p>


<h3>第５条（サーバルールの遵守）</h3>
<p>利用者は、全体サーバルール及び各サーバルールに定められた事項や関係する規定を遵守しなければなりません。</p>


<h3>第６条（サーバルールの対応範囲）</h3>
<p>全体サーバルールでの対応は、当サービス全てで共通して行われます。</p>

<p>２．各サーバルールでの対応は、そのルールが規定された所属サーバのみで行われます。</p>


<h3>第７条（サーバルールの掲示）</h3>
<p>全体サーバルールは、公式ホームページの「全体サーバルール」に掲示されます。</p>

<p>２．各サーバルールは、公式ホームページの「サーバー紹介」カテゴリに属する、所属サーバの紹介ページに個別で掲示されます。</p>

<p>３．全体サーバルール及び各サーバルールは、公式ホームページに掲示されている内容が最新のものとして取り扱います。</p>


<h3>第８条（制限等の解除申請）</h3>
<p>利用者は、本個別規定や利用規約などに違反したとして当サービス等の利用が制限され、その違反が事実でないと考える場合、当団体が定めた所定の様式に従い、制限等の解除申請を行うことができます。</p>

<p>２．制限等の解除申請は、該当する制限等が行われた日から起算して４５日以内のみ受け付けるものとします。</p>

<p>３．制限等の解除申請は、該当する制限等が行われたアカウントの所有者が行わなければならず、代理人などの本人以外の人が申請することはできません。</p>

<p>４．制限等の解除申請を行う場合、申請者は該当する制限等に関して、自身の正当性やその根拠となる事項を事実に基づき、当団体に提示しなければなりません。また、当団体が判断に必要として、提示を要求した事項については迅速に対応しなければなりません。</p>

<p>５．該当する制限等の解除申請は１回のみできることとし、結果が申請者に返却されることで対応が終了します。対応終了後は不服申し立てや再度の申請はできません。また、当団体が提示を要求した事項への対応が８日以上遅延した場合、行った制限等は妥当であるとし対応を終了します。</p>


<h3>第９条（個別規定の変更）</h3>
<p>当団体は、利用者に予告することなく本個別規定を変更することができるものとします。なお、本個別規定が変更された場合、利用開始日時に関わらず最新の個別規定に同意したものとみなします。</p>
